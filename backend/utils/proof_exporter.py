"""
Exportable Proof Artifacts — generates branded HTML/PDF reports.

4 report types:
  1. Score Report (22-dim dual score + callback + optional salary)
  2. Tailor Diff Report (before/after + change log)
  3. Coaching Report (session summary + per-question grades)
  4. Analytics Snapshot (funnel + trends + predictions)

Uses weasyprint for PDF if available, otherwise returns styled HTML.
"""

from __future__ import annotations

from datetime import datetime, UTC
from typing import Optional

# Check weasyprint availability
_WEASYPRINT_AVAILABLE = False
try:
    import weasyprint  # type: ignore
    _WEASYPRINT_AVAILABLE = True
except (ImportError, OSError):
    pass


def is_pdf_available() -> bool:
    return _WEASYPRINT_AVAILABLE


def _html_to_output(html: str) -> tuple[bytes, str]:
    """Convert HTML to PDF bytes if possible, otherwise return HTML bytes."""
    if _WEASYPRINT_AVAILABLE:
        pdf_bytes = weasyprint.HTML(string=html).write_pdf()
        return pdf_bytes, "application/pdf"
    return html.encode("utf-8"), "text/html"


